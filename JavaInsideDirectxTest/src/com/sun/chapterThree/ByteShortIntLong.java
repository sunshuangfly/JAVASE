package com.sun.chapterThree;
/**
 * 在主方法中创建不同数值型变量,相加后输出
 * @author Administrator
 *
 */
public class ByteShortIntLong {

	public static void main(String[] args) {
		byte byte1 = 124;
		short short1 = 32454;
		int int1 = 45784612;
		long long1 = 46789451;
		long result = byte1+short1+int1+long1;
		System.out.println(result);
	}
}
